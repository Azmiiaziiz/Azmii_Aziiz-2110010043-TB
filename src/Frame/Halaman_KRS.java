    private void tabel_KRSMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_KRSMouseClicked
try {
            koneksi.konn();
            int row =tabel_KRS.getSelectedRow();
            String tabel_klik=(tabel_KRS.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = koneksi.konn() .createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from krs where id_krs='"+tabel_klik+"'");
            if(sql.next()){
                String id = sql.getString("id_krs");
                tfidkrs.setText(id);
                String id_matkul = sql.getString("id_matakuliah");
                kbbkIdMatkul.setSelectedItem(id_matkul);
                String hari = sql.getString("hari");
                TFHari.setText(hari);
                 String semester = sql.getString("semester");
                TFsemester.setText(semester);
                String dosen = sql.getString("dosen");
                TFdosen.setText(dosen);               
                String kelas = sql.getString("kelas");
                TFKelas.setText(kelas);
                String jam = sql.getString("jam");
                TfJam.setText(jam);
            }
        } catch (Exception e) {}       
    }//GEN-LAST:event_tabel_KRSMouseClicked

