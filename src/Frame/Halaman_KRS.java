public void kombobox (){
try {
             String sql = "select * from matakuliah";
            java.sql.Statement stat = koneksi.konn().createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            
            while (hasil.next()){
             kbbkIdMatkul.addItem(hasil.getString("id_matakuliah"));
             
            }
            
        } catch (Exception e) {
        }}


public void isitabel( ){
    
     model.getDataVector( ).removeAllElements( );
     model.fireTableDataChanged( );

     try{
          
           Statement st = (Statement) koneksi.konn(). createStatement( );
           String sql   = "Select * from krs";
           ResultSet res   = st.executeQuery(sql);

           
           while(res.next ()){
                Object[ ] obj = new Object[7];
                obj[0] = res.getString("id_krs");
                obj[1] = res.getString("id_matakuliah");
                obj[2] = res.getString("dosen");
                obj[3] = res.getString("kelas");
                obj[4] = res.getString("semester");
                obj[5] = res.getString("jam");
                obj[6] = res.getString("hari");              

                model.addRow(obj);
            }
           
      }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage() );
      }
     
}





