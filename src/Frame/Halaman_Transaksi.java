    private void tabel_transaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_transaksiMouseClicked
try {
            koneksi.konn();
            int row =tabel_transaksi.getSelectedRow();
            String tabel_klik=(tabel_transaksi.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = koneksi.konn() .createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from transaksi where id_transaksi='"+tabel_klik+"'");
            if(sql.next()){
                String id = sql.getString("id_transaksi");
                tfidTransaksi.setText(id);
                String npm = sql.getString("npm");
                kbbkNPM.setSelectedItem(npm);
               
                 String pajak = sql.getString("pajak");
                TFPajak.setText(pajak);
                String biaya = sql.getString("biaya");
                TFbiaya.setText(biaya);               
                String total = sql.getString("total_keseluruhan");
                TFTotal.setText(total);
                
            }
        } catch (Exception e) {}       
    }//GEN-LAST:event_tabel_transaksiMouseClicked

