public void kombobox (){
try {
             String sql = "Select *from mahasiswa";
            java.sql.Statement stat = koneksi.konn().createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            
            while (hasil.next()){
             kbbkNPM.addItem(hasil.getString("npm"));
             
            }
            
        } catch (Exception e) {
        }}


public void isitabel( ){
    
     model.getDataVector( ).removeAllElements( );
     model.fireTableDataChanged( );

     try{
          
           Statement st = (Statement) koneksi.konn(). createStatement( );
           String sql   = "Select * from transaksi";
           ResultSet res   = st.executeQuery(sql);

           
           while(res.next ()){
                Object[ ] obj = new Object[5];
                obj[0] = res.getString("id_transaksi");                
                obj[1] = res.getString("npm");
                obj[2] = res.getString("biaya");
                obj[3] = res.getString("pajak");
                obj[4] = res.getString("total_keseluruhan");
                             

                model.addRow(obj);
            }
           
      }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage() );
      }
     
}





