    private void bttambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttambahActionPerformed
       try {
           if(tfidTransaksi.getText().equals("")){
            JOptionPane.showMessageDialog(null, " TIDAK BOLEH KOSONG");
        } else if (kbbkNPM.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(null, "  tidak boleh kosong");
        }else if (TFPajak.getText().equals("")){
            JOptionPane.showMessageDialog(null, "  tidak boleh kosong");
        }
        else if (TFbiaya.getText().equals("")){
            JOptionPane.showMessageDialog(null, "  tidak boleh kosong");
        }else if (TFTotal.getText().equals("")){
            JOptionPane.showMessageDialog(null, "  tidak boleh kosong");
        }
        else {
            String sql = "INSERT INTO transaksi VALUES ('"+tfidTransaksi.getText()+"','"+kbbkNPM.getSelectedItem()+"','"+TFbiaya.getText()+"','"+TFPajak.getText()+"','"+TFTotal.getText()+"')";
            java.sql.Connection conn=(Connection)koneksi.konn();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan");
            isitabel();
            clear();
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_bttambahActionPerformed

