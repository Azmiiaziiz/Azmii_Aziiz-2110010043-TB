    private void TFPajakKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFPajakKeyTyped
char c= evt.getKeyChar();
        if(! ((Character.isDigit(c)) ||
            ( c== KeyEvent.VK_BACK_SPACE)
            || (c== KeyEvent.VK_DELETE)
        ))
        {
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Hanya angka 1-9");
            TFPajak.requestFocus();
            evt.consume();
        }       
    }//GEN-LAST:event_TFPajakKeyTyped

